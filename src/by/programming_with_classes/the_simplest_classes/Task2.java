package by.programming_with_classes.the_simplest_classes;

public class Task2 {
    private int able;
    private int enable;

    public Task2(int able, int enable) {
        this.able = able;
        this.enable = enable;
    }

    public Task2() {
    }

    public int getAble() {
        return able;
    }

    public void setAble(int able) {
        this.able = able;
    }

    public int getEnable() {
        return enable;
    }

    public void setEnable(int enable) {
        this.enable = enable;
    }
}
